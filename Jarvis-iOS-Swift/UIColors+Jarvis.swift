import UIKit

extension UIColor {
    
    class func jarvis_lightBlue() -> UIColor {
        return UIColor(red: 186.0 / 255.0, green: 228.0 / 255.0, blue: 229.0 / 255.0, alpha: 1.0)
    }
    
    class func jarvis_gunMetal() -> UIColor {
        return UIColor(red: 38.0 / 255.0, green: 40.0 / 255.0, blue: 38.0 / 255.0, alpha: 1.0)
    }
    
}
